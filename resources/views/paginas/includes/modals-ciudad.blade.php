        <h4 class="modal-title" id="myModalLabel">Teatro Cine Italiano</h4>
      </div>
      <div class="modal-body">
        <p class="">
      Este Teatro fue inaugurado en 1918, por el constructor Gino Peressini como lugar de encuentro de inmigrantes italianos afianzados en nuestra ciudad. Su estilo es a la italiana y alberga alrededor de 350 personas. Después de estar varios años cerrados, en el 2015 re abrió sus puertas, totalmente renovado. Actualmente en su interior funciona la Dirección de Cultura y el fin de semana la oficina de Información Turística. Semanalmente la gente de la ciudad puede disfrutar de los estrenos de películas argentinas. 
<strong>Fue declarado dentro del sector de Preservación Histórico Patrimonial bajo Ord. Mun. N°2074/01 y Patrimonio Histórico, Cultural y Arquitectónico de Lobos por Ord. Mun. 2044/00.</strong>
