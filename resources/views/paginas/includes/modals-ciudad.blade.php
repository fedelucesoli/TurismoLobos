        <button type="button" class="close" data-dismiss="modal" aria-label="Cerrar">
        <span aria-hidden="true">&times;</span></button>
        <h4 class="modal-title" id="myModalLabel">Plaza 1810</h4>
      </div>
      <div class="modal-body">
        <p class="">
         Es la plaza principal de la Ciudad, está ubicada frente a la Parroquia Nuestra Señora del Carmen y el Palacio Municipal. Su origen data de 1811 y los terrenos fueron donados por el primer Intendente del partido, Manuel Antonio Caminos. La plaza se llamó "Buenos Aires" pero luego, al cumplirse el centenario de la Revolución de Mayo, se le cambió el  nombre por el actual.
<br>En su centro se destaca el monumento a la Madre construido en 1962 por el escultor Luis Perlotti, y en 2005 se construye la fuente en la cual se encuentra actualmente.
<br>
<strong>Fue declarado Lugar Histórico Nacional (Decreto 1020/2004).</strong>
        </p>
      </div>

