        <h4 class="modal-title" id="myModalLabel">Club Social Lobense</h4>
      </div>
      <div class="modal-body">
        <p class="">
         El club se fundó el 1 de Marzo 1894, con el objeto de formar una escuela musical. Una vez organizada, la misma se desarrolla bajo la dirección del Maestro Emilio Zarini, uno de los primeros músicos locales. Este edificio fue inaugurado en 1932, es de dos plantas, con fachada neoclásica y alguna influencia art decó.
<br>Actualmente posee un salón para uso de eventos sociales y culturales. En su planta baja, se encuentra un Restaurante.
<br>
<strong>Fue declarado dentro del sector de Preservación Histórico Patrimonial bajo Ord. Mun. N°2074/01 y Monumento Histórico Nacional (Decreto 1020/2004).</strong>
